package com.mypet.mypet.core.entities.tb_pk;

import com.mypet.mypet.core.entities.tb_principal.Contatos;
import com.mypet.mypet.core.entities.tb_principal.Pessoas;
import jakarta.persistence.*;
import lombok.*;

import java.io.Serializable;
@ToString
@Data
@Setter
@Getter
@Builder
@NoArgsConstructor
@AllArgsConstructor
@EqualsAndHashCode(of = {"id"})
@Entity
@Table(name = "PessoaContatoPK")
@Embeddable
public class PessoaContatoPK implements Serializable{



        private static final long serialVersionUID = 1l;



        @GeneratedValue(strategy = GenerationType.IDENTITY)

        private long id;
        @ManyToOne
        @JoinColumn(name = "PessoaContatoName")
        private Pessoas name;
        @ManyToOne
        @JoinColumn(name = "PessoaContatoLastName")
        private Pessoas lastName;
        @ManyToOne
        @JoinColumn(name = "PessoaContatoCpf")
        private Pessoas cpf;
        @ManyToOne
        @JoinColumn(name = "ListContato")
        private Contatos ListContatos;



}
